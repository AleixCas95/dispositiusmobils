public class User {
    private String name, lastname, handle, following,
            followers, about;

    public String getName() {
        return name;
    }

    public String getLastname() {
        return lastname;
    }

    public String getHandle() {
        return handle;
    }

    public String getFollowing() {
        return following;
    }

    public String getFollowers() {
        return followers;
    }

    public String getAbout() {
        return about;
    }
}
